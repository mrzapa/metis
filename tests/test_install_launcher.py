class TestReadmeDocs:
    """Tests for README.md launch command documentation."""

    def test_readme_documents_metis_default_as_web(self) -> None:
        """README should document `metis` as opening web UI."""
        content = README.read_text(encoding="utf-8")
        assert "metis" in content.lower()
        web_match = re.search(
            r"\|\s*\*\*Web UI\*\*\s*\|\s*`metis`\s*\|",
            content,
        )
        assert web_match, (
            "README should have Web UI as first entry with `metis` command"
        )

    def test_readme_documents_desktop_override(self) -> None:
        """README should document --desktop/--gui as Qt GUI override."""
        content = README.read_text(encoding="utf-8")
        assert "--desktop" in content, "README should document --desktop"
        assert "--gui" in content, "README should document --gui"

    def test_readme_documents_cli_mode(self) -> None:
        """README should document --cli for CLI mode."""
        content = README.read_text(encoding="utf-8")
        assert "--cli" in content, "README should document --cli"

    def test_readme_no_stale_gui_mode_reference(self) -> None:
        """README should not say 'metis' alone opens GUI (stale reference)."""
        content = README.read_text(encoding="utf-8")
        stale_patterns = [
            r'metis\s*(?:#.*?)?\s*["\']?\s*#.*?GUI mode\b',
            r'metis\s*(?:#.*?)?\s*["\']?\s*#.*?Desktop GUI\b',
        ]
        for pattern in stale_patterns:
            match = re.search(pattern, content, re.IGNORECASE)
            assert not match, (
                f"README has stale reference: {match.group() if match else ''}"
            )

    def test_readme_no_stale_web_flag_reference(self) -> None:
        """README should not present --web as preferred user mode."""
        content = README.read_text(encoding="utf-8")
        lines = content.split("\n")
        for line in lines:
            if "--web" in line and "metis" in line:
                assert "default" not in line.lower(), (
                    f"README should not present --web as default: {line.strip()}"
                )
